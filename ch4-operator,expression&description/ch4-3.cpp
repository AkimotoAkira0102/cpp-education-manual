#include <iostream>			//include iostream files
#include <cstdlib>			//include cstdlib files
using namespace std;		//use namespace std

int main (void){			//begining of main block
	int a=16;				//declaring a is a integer and equal to 16
	int b=7;				//declaring b is a integer and equal to 7
	cout << "a=" << a << ", b="	<< b << endl;		//print a & b
	cout << "a/b=" << a/b << endl;		//print a/b
	cout << "(float)a/b=" << (float)a/b << endl;	//print (float)a/b
	system ("pause");		//pause the program
	return 0;				//return integer 0
} 							//end of main block
